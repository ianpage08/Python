# Exemplo de uso de frozenset
# frozenset é uma versão imutável de um set, ou seja, não permite adições, remoções ou alterações de elementos após sua criação.
# É útil quando você precisa garantir que a coleção de elementos permaneça constante e não seja modificada.
frozenset_exemplo = frozenset({'Python', 'Java', 'C++', 'JavaScript', 'Ruby', 'Python'})